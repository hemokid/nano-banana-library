/** Get a record by id */
function getById(id) {
  return PROMPTS.find(p => p.id === id);
}

/** Get all records belonging to a category ('All' returns everything) */
function getByCategory(category) {
  if (category === 'All') return PROMPTS;
  return PROMPTS.filter(p => p.category === category);
}

/** Return the variant group key for a record, or null */
function getVariantGroup(record) {
  if (!record.variantGroup) return null;
  return VARIANT_GROUPS[record.variantGroup];
}

/** Build the card-level entries for the grid.
 *  Variant groups appear once (using first variant as card face).
 *  Standalone records appear individually.
 */
function getCardEntries(category) {
  const records = getByCategory(category);
  const seen = new Set();
  const entries = [];

  for (const rec of records) {
    if (rec.variantGroup) {
      const groupKey = rec.variantGroup;
      if (seen.has(groupKey)) continue;
      seen.add(groupKey);
      // Use first variant that exists in the filtered set as the card face
      const group = VARIANT_GROUPS[groupKey];
      const firstInSet = group.variants.find(v => records.some(r => r.id === v.id));
      if (firstInSet) {
        entries.push({ type: 'group', groupKey, leadId: firstInSet.id });
      }
    } else {
      entries.push({ type: 'single', id: rec.id });
    }
  }
  return entries;
}

/** Count visible cards for a category */
function getCardCount(category) {
  return getCardEntries(category).length;
}
